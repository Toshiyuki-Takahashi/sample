# 変数
# 値を入れておく箱のようなもの。
# ┗後からいつでも取り出せる。
#
# 変更も最小限で全体を変更可能となるため
# 開発工数が最小限になる。
#
# 例）使い方
# numの中に11を格納する。
num = 11

# numの値を出力
puts num

# 円の面積
radius = 5
# radiusは半径のこと。
puts radius * radius * 3.14

